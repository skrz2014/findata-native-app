-- Register warehouse reference callback
CREATE OR REPLACE PROCEDURE CORE.REGISTER_WAREHOUSE_REF(ref_name STRING, operation STRING, ref_or_alias STRING)
RETURNS STRING
LANGUAGE SQL
AS
BEGIN
    CASE (operation)
        WHEN 'ADD' THEN
            SELECT SYSTEM$SET_REFERENCE(:ref_name, :ref_or_alias);
            INSERT INTO CONFIG.BOUND_REFERENCES (REFERENCE_NAME, OBJECT_NAME)
                VALUES (:ref_name, :ref_or_alias);
        WHEN 'REMOVE' THEN
            SELECT SYSTEM$REMOVE_REFERENCE(:ref_name, :ref_or_alias);
            DELETE FROM CONFIG.BOUND_REFERENCES WHERE REFERENCE_NAME = :ref_name;
        WHEN 'CLEAR' THEN
            SELECT SYSTEM$REMOVE_ALL_REFERENCES(:ref_name);
            DELETE FROM CONFIG.BOUND_REFERENCES WHERE REFERENCE_NAME = :ref_name;
    END CASE;
    RETURN 'OK';
END;

GRANT USAGE ON PROCEDURE CORE.REGISTER_WAREHOUSE_REF(STRING, STRING, STRING)
    TO APPLICATION ROLE APP_ADMIN;

-- Get app configuration
CREATE OR REPLACE PROCEDURE CORE.GET_CONFIG(key_name STRING)
RETURNS STRING
LANGUAGE SQL
AS
BEGIN
    LET result STRING := '';
    SELECT SETTING_VALUE INTO :result
        FROM CONFIG.APP_SETTINGS
        WHERE SETTING_KEY = :key_name;
    RETURN result;
END;

GRANT USAGE ON PROCEDURE CORE.GET_CONFIG(STRING) TO APPLICATION ROLE APP_ADMIN;
GRANT USAGE ON PROCEDURE CORE.GET_CONFIG(STRING) TO APPLICATION ROLE APP_ANALYST;

-- Update app configuration
CREATE OR REPLACE PROCEDURE CORE.SET_CONFIG(key_name STRING, key_value STRING)
RETURNS STRING
LANGUAGE SQL
AS
BEGIN
    UPDATE CONFIG.APP_SETTINGS
        SET SETTING_VALUE = :key_value,
            UPDATED_AT = CURRENT_TIMESTAMP(),
            UPDATED_BY = CURRENT_USER()
        WHERE SETTING_KEY = :key_name;

    INSERT INTO CONFIG.AUDIT_LOG (EVENT_TYPE, EVENT_DETAIL)
        VALUES ('CONFIG_UPDATE', :key_name || ' = ' || :key_value);

    RETURN 'Configuration updated';
END;

GRANT USAGE ON PROCEDURE CORE.SET_CONFIG(STRING, STRING) TO APPLICATION ROLE APP_ADMIN;

-- Health check procedure
CREATE OR REPLACE PROCEDURE CORE.HEALTH_CHECK()
RETURNS VARIANT
LANGUAGE SQL
AS
BEGIN
    LET price_count INTEGER := 0;
    LET fundamental_count INTEGER := 0;
    LET risk_count INTEGER := 0;
    LET latest_price_date DATE;

    SELECT COUNT(*) INTO :price_count FROM CORE.MARKET_PRICES;
    SELECT COUNT(*) INTO :fundamental_count FROM CORE.COMPANY_FUNDAMENTALS;
    SELECT COUNT(*) INTO :risk_count FROM CORE.RISK_METRICS;
    SELECT MAX(TRADE_DATE) INTO :latest_price_date FROM CORE.MARKET_PRICES;

    LET health_status STRING := 'OK';
    IF (:price_count = 0) THEN
        health_status := 'FAILED';
    END IF;

    CALL SYSTEM$REPORT_HEALTH_STATUS(:health_status);

    RETURN OBJECT_CONSTRUCT(
        'status', health_status,
        'market_prices_count', price_count,
        'fundamentals_count', fundamental_count,
        'risk_metrics_count', risk_count,
        'latest_price_date', latest_price_date,
        'checked_at', CURRENT_TIMESTAMP()
    );
END;

GRANT USAGE ON PROCEDURE CORE.HEALTH_CHECK() TO APPLICATION ROLE APP_ADMIN;

-- Audit log viewer
CREATE OR REPLACE PROCEDURE CORE.VIEW_AUDIT_LOG(limit_rows INTEGER)
RETURNS TABLE(LOG_ID NUMBER, EVENT_TYPE VARCHAR, EVENT_DETAIL VARCHAR, CREATED_AT TIMESTAMP_NTZ, CREATED_BY VARCHAR)
LANGUAGE SQL
AS
BEGIN
    LET res RESULTSET := (
        SELECT LOG_ID, EVENT_TYPE, EVENT_DETAIL, CREATED_AT, CREATED_BY
        FROM CONFIG.AUDIT_LOG
        ORDER BY CREATED_AT DESC
        LIMIT :limit_rows
    );
    RETURN TABLE(res);
END;

GRANT USAGE ON PROCEDURE CORE.VIEW_AUDIT_LOG(INTEGER) TO APPLICATION ROLE APP_ADMIN;

